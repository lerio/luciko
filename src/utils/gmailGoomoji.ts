const GMAIL_GOOMOJI_HOSTNAME = "mail.google.com";
const GMAIL_GOOMOJI_PATH_PREFIX = "/mail/e/";

const GMAIL_GOOMOJI_MAP = new Map(
  RAW_GMAIL_GOOMOJI_MAP.trim()
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const [code, emoji] = line.split(/\s+/, 3);
      return [code.toUpperCase(), emoji] as const;
    }),
);

function getGmailGoomojiCode(url: string): string | null {
  try {
    const parsed = new URL(url);
    if (
      parsed.hostname !== GMAIL_GOOMOJI_HOSTNAME ||
      !parsed.pathname.startsWith(GMAIL_GOOMOJI_PATH_PREFIX)
    ) {
      return null;
    }

    const code = parsed.pathname.slice(GMAIL_GOOMOJI_PATH_PREFIX.length).split("/")[0];
    return code ? code.toUpperCase() : null;
  } catch {
    return null;
  }
}

export function isGmailGoomojiUrl(url: string) {
  return getGmailGoomojiCode(url) !== null;
}

export function resolveGmailGoomoji(url: string) {
  const code = getGmailGoomojiCode(url);
  if (!code) return null;
  return GMAIL_GOOMOJI_MAP.get(code) ?? null;
}
